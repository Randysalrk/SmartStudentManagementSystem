    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
       try{
          Class.forName("com.mysql.jdbc.Driver");
          Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/sms","root","");
          
          String sql = "select * from lecture where LecID = ?";
          PreparedStatement pstmt = conn.prepareStatement(sql);
          pstmt.setString(1, LecID.getText());
          
          ResultSet search = pstmt.executeQuery();
          if(search.next())
          {
            FullName.setText(search.getString("Fullname"));
            IDNo.setText(search.getString("IDNo"));
            Address.setText(search.getString("Address"));
            DOB.setText(search.getString("DOB"));
            CourseName.setSelectedItem(search.getString("CourseName"));
            Password.setText(search.getString("Password"));
          }
          else{
            JOptionPane.showMessageDialog(null, "Record Not Found");
          }
          conn.close();
          
        }catch(Exception e)
        {
          JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_SearchButtonActionPerformed

