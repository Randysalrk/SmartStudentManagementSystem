          PreparedStatement pstmt = conn.prepareStatement(sql);
          pstmt.setString(1, FullName.getText());
          pstmt.setString(2, IDNo.getText());
          pstmt.setString(3, Address.getText());
          pstmt.setString(4, DOB.getText());          
